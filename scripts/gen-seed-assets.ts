/**
 * Generate worker/seed-assets.generated.ts — the travelkit MCP config + settings + skill
 * tree, inlined as strings so the Worker can push them into each per-user sandbox's /code
 * via fetch (the sandbox file API). Run before build/deploy: `pnpm gen:seed`.
 *
 * The output is gitignored (it embeds the travelkit token from .mcp.json) and rebuilt from
 * the local files each time.
 */
import { readFileSync, readdirSync, statSync, writeFileSync } from 'node:fs'
import { join } from 'node:path'

const ROOT = process.cwd()

function walk(dir: string): string[] {
  const out: string[] = []
  for (const name of readdirSync(dir)) {
    const p = join(dir, name)
    if (statSync(p).isDirectory()) out.push(...walk(p))
    else out.push(p)
  }
  return out
}

const files: Record<string, string> = {}

// .mcp.json (travelkit MCP, with token) → /code/.mcp.json
files['.mcp.json'] = readFileSync(join(ROOT, '.mcp.json'), 'utf8')

// Pre-trust the project MCP so the sandbox's headless claude actually loads travelkit
// (mirrors server/rebyte/seed.ts).
files['.claude/settings.json'] = JSON.stringify({ enableAllProjectMcpServers: true }, null, 2)

// The travelkit skill tree.
const skillRoot = join(ROOT, '.claude', 'skills', 'travelkit')
for (const abs of walk(skillRoot)) {
  const rel = '.claude/skills/travelkit/' + abs.slice(skillRoot.length + 1).split('\\').join('/')
  files[rel] = readFileSync(abs, 'utf8')
}

const out =
  `// AUTO-GENERATED by scripts/gen-seed-assets.ts — do not edit. Gitignored (embeds the\n` +
  `// travelkit token). The Worker writes these into each per-user sandbox's /code via fetch.\n` +
  `export const SEED_FILES: Record<string, string> = ${JSON.stringify(files, null, 2)}\n`

writeFileSync(join(ROOT, 'worker', 'seed-assets.generated.ts'), out)
console.log(`wrote worker/seed-assets.generated.ts — ${Object.keys(files).length} files`)
